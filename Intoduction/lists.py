
# Creating a list   
my_list = [4, "Hello", 3.14]

# Accessing list elements
print(my_list[1])  # Output: Hello

# Adding elements
my_list.append("Python")
my_list.insert(1, "there")

# Removing elements
my_list.remove("there")  # Removes the first occurrence of "there"

# Iterating over a list
for element in my_list:
    print(element)
